//Relational operator

public class relational_operator {
  public static void main(Creation_string args[])
  {
    //== comparison operator
    int a = 10;
    int b = 10;
    System.out.println("a is eqaul to b:"+(a==b));
    //!= does not equal to operator
    System.out.println("a is does eqaul to b:"+(a!=b));
    //> greater than operator
    System.out.println("a is greater than b:"+(a>b));
    //< less than
    System.out.println("a is less than b:"+(a<b));
    //>= greater and equal to operator
    System.out.println("a is greater than and eqaul to b:"+(a>=b));
    //<= less than and equal to operator
    System.out.println("a is less eqaul to b:"+(a<=b));
    }  
}
